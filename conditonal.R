age <- 111

if(age >= 1 && age <= 3){
  
  print("You Are A Baby")
  
}else if(age >= 4 && age <= 12){
  
  print("You Are A Child")
  
}else if(age >= 13 && age <= 19){
  
  print("You Are A Teenager")
  
}else if(age >= 20 && age <= 65){
  
  print("You Are An Adult")
  
}else if(age >= 66 && age <= 120){
  
  print("You Are Elderly")
  
}else{
  
  print("Invalid Age")
  
}